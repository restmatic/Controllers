import {map, get, isFunction, reduce, fromPairs, toPairs} from 'lodash/fp'
import {CreatePlugin} from "@pomegranate/plugin-tools";
const tsGenerator = `// Generated by the Pomegranate cli on {{creationDate}}

// name: {{name}}

export const Controller = (PluginLogger) => {
  let names = []
  return {
    addName: (name) => {
      names.push(name)
    },
    getNames: () => {
      return names
    }
  }
}
`

export interface RestmaticController {

}

export interface RestmaticControllers {
  [key: string]: RestmaticController
}

export const Plugin = CreatePlugin('anything')
  .configuration({
    name: 'Controllers',
    injectableParam: 'Controllers',
    provides: ['@restmatic/Core'],
    optional: ['SQL']
  })
  .variables({})
  .directories([{path: '.', prop: 'main'}])
  .hooks({
    load: async (Injector, PluginFiles, PluginLogger) => {
      PluginLogger.log('Creating Controllers', 2)

      let files = await PluginFiles('main').fileList({ext: '.js'})
      let build = map((file) => {
        let required = require(file.path)
        let fileName = file.getBaseName()

        PluginLogger.log(`Creating Controller: ${fileName}.`)

        let mw = get('Controller', required)
        if (!mw) {
          throw new Error(`Controller file ${fileName} does not contain an export on the Controller property.`)
        }
        if (!isFunction(mw)) {
          throw new Error(`Controller file ${fileName} does not export an injectable function on the Controller property.`)
        }
        return [file.getBaseName(), Injector.inject(mw)]
      }, files)

      return reduce((acc, [name, obj]) => {
        acc[name] = obj
        return acc
      }, {}, build)

    }
  })
  .commands(function (PomConfig, PluginFiles, Handlebars) {
    return (yargs) => {
      return yargs
        .usage('usage: $0')
        .command({
          command: 'generate <name>',
          aliases: 'g',
          describe: `Generates Controller file <name>`,
          builder: (yargs) => {
            return yargs
              .positional('name', {
                describe: 'The the filename to be created.',
                default: 'index',
                type: 'string'
              })
              .option('l', {
                alias: 'language',
                describe: 'Generate TypeScript or Javascript',
                default: 'ts',
                choices: ['ts'],
                type: 'string'
              })
              .option('force', {
                alias: 'f',
                default: false,
                describe: 'overwrites the specified file if it exists.',
                type: 'boolean'
              })
          },
          handler: async (argv) => {
            let Pf = PluginFiles('main')
            let file = `${argv.name}.${argv.language}`
            let exists = await Pf.projectFileExists(file)
            let compile = Handlebars.compile(tsGenerator)
            let compiled = compile({creationDate: new Date().toDateString(), name: argv.name})

            if (exists && !argv.force) {
              throw new Error(`${file} \n exists \n Rerun with --force to overwrite.`)
            }
            await Pf.outputProjectFile(file, compiled)
            console.log(`Created @restmatic/Controllers Controller file ${file}`)
          }
        })
        .help()
    }
  })